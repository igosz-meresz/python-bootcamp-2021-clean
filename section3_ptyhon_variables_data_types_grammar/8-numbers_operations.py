import math

# Arithmetic Operations
print(23 + 12)
print(50 - 10)
print(50 * 10)
print(50 / 10)
print(50 // 4.5)
print(50 % 6)
print(2 ** 5)

# Augmented Assignment Operator
number = 10
number = number + 5
number += 5
number *= 2
number /= 5
print(number)

# Number Methods

# 1- round()
print(round(4.8685))
print(round(3.2155))

# 2- abs()
print(abs(-1.45))
print(abs(+1.58))

# https://docs.python.org/3/library/math.html

print(math.sin(180))
print(math.sin(0))
print(math.cos(0))
print(math.ceil(1.4))  # rounds up
print(math.ceil(2.03))  # rounds up
